-- ------------------------------------------------------------------------------
-- VIEW: LEADERBOARD_RANKED (ranking dinamico por pontos)
-- ------------------------------------------------------------------------------
CREATE OR REPLACE VIEW public.leaderboard_ranked AS
SELECT
    ROW_NUMBER() OVER (ORDER BY total_points DESC, fid ASC) AS rank,
    l.*
FROM public.leaderboard l
ORDER BY rank ASC;

-- ------------------------------------------------------------------------------
-- VIEW: PROFILES_RANKED (ordem visual tipo Excel por points)
-- ------------------------------------------------------------------------------
CREATE OR REPLACE VIEW public.profiles_ranked AS
SELECT
    ROW_NUMBER() OVER (ORDER BY points DESC, fid ASC) AS linha,
    p.*
FROM public.profiles p
ORDER BY linha ASC;

