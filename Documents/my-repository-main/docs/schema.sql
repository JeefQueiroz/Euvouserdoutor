-- Garante internal_id mesmo em bases antigas
ALTER TABLE public.profiles ADD COLUMN IF NOT EXISTS internal_id BIGINT;
CREATE SEQUENCE IF NOT EXISTS public.profiles_internal_id_seq;
ALTER TABLE public.profiles ALTER COLUMN internal_id SET DEFAULT nextval('public.profiles_internal_id_seq');
UPDATE public.profiles
SET internal_id = nextval('public.profiles_internal_id_seq')
WHERE internal_id IS NULL;
SELECT setval(
  'public.profiles_internal_id_seq',
  COALESCE((SELECT MAX(internal_id) FROM public.profiles), 1),
  true
);

CREATE INDEX IF NOT EXISTS idx_profiles_wallet_address ON public.profiles(wallet_address);
CREATE UNIQUE INDEX IF NOT EXISTS idx_profiles_internal_id_unique ON public.profiles(internal_id);

-- ------------------------------------------------------------------------------
-- TABELA: CHECKINS (Engajamento Diario)
-- ------------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.checkins (
    fid BIGINT PRIMARY KEY,
    streak INTEGER DEFAULT 0,
    best_streak INTEGER DEFAULT 0,
    last_date DATE,
    history JSONB DEFAULT '[]'::jsonb,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

ALTER TABLE public.checkins ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Permitir tudo para a API" ON public.checkins;
CREATE POLICY "Permitir tudo para a API" ON public.checkins
    FOR ALL USING (true) WITH CHECK (true);

